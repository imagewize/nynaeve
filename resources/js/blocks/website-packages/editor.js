import { addFilter } from '@wordpress/hooks';
import { createHigherOrderComponent } from '@wordpress/compose';
import { InspectorControls } from '@wordpress/block-editor';
import { PanelBody, ToggleControl } from '@wordpress/components';

const withInspectorControls = createHigherOrderComponent((BlockEdit) => {
    return (props) => {
        if (props.name !== 'nynaeve/website-packages') {
            return <BlockEdit {...props} />;
        }

        return (
            <>
                <BlockEdit {...props} />
                <InspectorControls>
                    <PanelBody title="Custom Settings">
                        <ToggleControl
                            label="Example Toggle"
                            checked={props.attributes.exampleToggle}
                            onChange={(value) => props.setAttributes({ exampleToggle: value })}
                        />
                    </PanelBody>
                </InspectorControls>
            </>
        );
    };
}, 'withInspectorControls');

addFilter('editor.BlockEdit', 'nynaeve/with-inspector-controls', withInspectorControls);